    public static string Get(string key)
    {
        if (string.IsNullOrEmpty(key))
            return DefaultHint;

        if (Map.TryGetValue(key, out string text))
            return text;

        if (key.StartsWith("切换到 "))
            return "切换当前玩家控制的车辆，并同步读取该车的物理参数。";

        return DefaultHint;
    }

